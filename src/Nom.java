
public class Nom {
    private String name;
    private int id;
    public Nom(String name, int id) {
        this.name = name;
        this.id = id;
    }
    public String getNom() {
        return name;
    }
    public int getId() {
        return id;
    }
    public void setNom(String name) {
        this.name = name;
    }
    public void setId(int id) {
        this.id = id;
    }
    
}
